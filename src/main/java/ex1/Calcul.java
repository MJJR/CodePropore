package ex1;

/**Class Calcul
 * @author matth
 */
public class Calcul {

	/** Fonction : Additionne les deux entiers en entrée
	 * @param a
	 * @param b
	 * @return
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/** Fonction : Soustrait les deux entiers en entrée
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
